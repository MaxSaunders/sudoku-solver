def solve(bo):
    find = find_empty(bo)
    if not find:
        return True
    else:
        row, col = find

    for i in range(1,10):
        if valid(bo, i, (row, col)):
            bo[row][col] = i

            if solve(bo):
                return True

            bo[row][col] = 0

    return False


def valid(bo, num, pos):
    # Check row
    for i in range(len(bo[0])):
        if bo[pos[0]][i] == num and pos[1] != i:
            return False

    # Check column
    for i in range(len(bo)):
        if bo[i][pos[1]] == num and pos[0] != i:
            return False

    # Check section
    box_x = pos[1] // 3
    box_y = pos[0] // 3

    for i in range(box_y * 3, box_y * 3 + 3):
        for j in range(box_x * 3, box_x * 3 + 3):
            if bo[i][j] == num and (i, j) != pos:
                return False

    return True

# Iterates over the 2D array representing a sudoku board
# to print out a visual representation
# Parameters: 2D Array of a sudoku board
def print_board(bo):
    for i in range(len(bo)):
        if i % 3 == 0 and i != 0:
            print("- - - - - - - - - - -")

        # print(*bo[0])
        for j in range(len(bo[i])):
            if j % 3 == 0 and j != 0:
                print("| ", end='')

            if j == (len(bo[i]) - 1):
                print(bo[i][j])
            else:
                print(str(bo[i][j]) + " ", end='')
        # print("\n")

# Find an empty cell in the board
# Parameters: 2D Array of a sudoku board with 0's representing empty cells
# Returns: (x, y) coordinates of the empty cell
def find_empty(bo):
    for i in range(len(bo)):
        for j in range(len(bo[0])):
            if bo[i][j] == 0:
                return (i, j)

    return None

